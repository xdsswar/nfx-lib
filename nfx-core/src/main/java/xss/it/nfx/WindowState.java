public enum WindowState {
    /**
     * Represents the normal window state.
     */
    NORMAL,

    /**
     * Represents the minimized window state.
     */
    MINIMIZED,

    /**
     * Represents the maximized window state.
     */
    MAXIMIZED,

    /**
     * Represents the full screen window state.
     */
    FULL_SCREEN
}
